public class TempConverter {
	public static void main(String[] args) {
		double c = 12; // C is for Celsius
		double f = c * 9/5 + 32;
		System.out.print("When it is " + c + " Celsius, it is ");
		System.out.print(f + " fahrenheit.");
	}

}
